    private void btnBuscarTacoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarTacoActionPerformed
        try {
            int idtacoBuscado = Integer.parseInt(txtIdTaco.getText());
            //controladortaco.buscarTaco(idtacoBuscado, dtmTortilla, dtmSalsa, dtmAlimento, tblTortilla, tblAlimentos, tblSalsa);
            controladortaco.buscarTaco(idtacoBuscado, dtmTortilla, dtmSalsa, dtmAlimento, tblTortilla, tblAlimentos, tblSalsa);
            //for (int i = 0; i < 2; i++) {
                //System.out.println("id array: " + arrayy[i]);
            //}

            double importe = controladortaco.obtenerImporteAlRealizarBusqueda(idtacoBuscado);
            if (importe == 0.0) {
                JOptionPane.showMessageDialog(null, "El Taco N° " + idtacoBuscado + " NO EXISTE!!!", "ERROR", JOptionPane.ERROR_MESSAGE);
                btnEliminar.setEnabled(false);
                btnModificar.setEnabled(false);
            }
            else{
               String importeS = String.valueOf(importe);
               //System.out.println("importe string:" + importeS);
               lbTotalTaco.setText(importeS);
               btnEliminar.setEnabled(true);
               btnModificar.setEnabled(true);
            }
            

            if (dtmTortilla.getRowCount() == 2) {
                this.opcSimple.setSelected(false);
                this.opcDoble.setSelected(true);
            } else {
                this.opcSimple.setSelected(true);
                this.opcDoble.setSelected(false);
            }
            

        } catch (NumberFormatException nfe) {
            JOptionPane.showMessageDialog(null, "Debe ingresar un N° de TACO en el buscador!!", "ERROR", JOptionPane.ERROR_MESSAGE);  
        }
    }//GEN-LAST:event_btnBuscarTacoActionPerformed
    private void limpiarTablas() {
        this.dtmTortilla.setRowCount(0);
        this.dtmAlimento.setRowCount(0);
        this.dtmSalsa.setRowCount(0);
        lbTotalTaco.setText("0.0");
        comboAlimentos.setSelectedItem("<Seleccione>");
        comboTortilla.setSelectedItem("<Seleccione>");
        comboSalsa.setSelectedItem("<Seleccione>");
        txtIdTaco.setText("");

    }

    private void cargarFilaDeTabla(JTable tabla, JComboBox combobox, DefaultTableModel dtm) {
        String Lista = (String) combobox.getSelectedItem();
        if ("<Seleccione>" != combobox.getSelectedItem()) {
            String[] Registro = Lista.split("_");
            dtm.addRow(new Object[]{Registro[0], Registro[1], Registro[2]});

            tabla.setModel(dtm);
            String total_tacos = Double.toString(Double.valueOf(lbTotalTaco.getText()) + Double.valueOf(Registro[2]));
            lbTotalTaco.setText(total_tacos);
        }
    }
