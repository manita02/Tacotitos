    private void estiloTablas(DefaultTableModel dtm, JTable tabla) {
        String[] titulos = new String[]{"Código", "Nombre", "Precio $$$"}; //titulos de la tabla
        dtm.setColumnIdentifiers(titulos);

        tabla.setFont(new java.awt.Font("Segoe UI Semibold", 0, 12));
        JTableHeader th;
        th = tabla.getTableHeader();
        th.setFont(new java.awt.Font("Segoe UI Semibold", 0, 14));

        tabla.setModel(dtm);

    }

    private void eliminarFilaDeTabla(DefaultTableModel dtm, JTable tabla) {
        String total_tacos = Double.toString(Double.valueOf(lbTotalTaco.getText()) - Double.valueOf(tabla.getValueAt(tabla.getSelectedRow(), 2).toString()));
        lbTotalTaco.setText(total_tacos);
        dtm.removeRow(tabla.getSelectedRow());
        tabla.setModel(dtm);
    }

